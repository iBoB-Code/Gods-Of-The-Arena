export const getTypeImg = (type) => {
  switch (type) {
  case 'archer':
    return 'archer';
  case 'swordman':
    return 'sword';
  case 'horseman':
    return 'horse';
  case 'spearman':
    return 'spear';
  case 'animal':
    return 'animal';
  default:
    return 'def';
  }
};

